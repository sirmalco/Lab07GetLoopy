public class PartA
{
    public static void main(String[] args)
    {
        System.out.println("Task 1");

        for(int i = 0; i<=30; i++)
        {
            System.out.print(i);
            System.out.print(" ");
        }
        System.out.println();

        System.out.println("Task 2");

        for(int i = 30; i>=0; i--)
        {
            System.out.print(i);
            System.out.print(" ");
        }
        System.out.println();

        System.out.println("Task 3");

        for(int i = 0; i<=18; i++)
        {

            System.out.print(i);
            i=i+2;
            System.out.print(" ");
        }
        System.out.println();
        System.out.println("Task 4");
        for(int i =10; i>=0; i--)
        {

            System.out.print(i);
            i--;
            System.out.print(" ");

        }
    }
}
